<h1>lsBootstrap</h1>
<h2>A simple framework for building websites</h2>

<p>
  lsBootstrap is a frontend-framework which I use to build websites. It is under
  current development, and I will add some features over time.<br/>
  <br/>
  I'm very sorry, but at this time there's no documentation available (and
  probably never will be). But the code should be pretty self-explanatory. If
  not, don't hesitate to contact me over at
  <a href="http://larsschweisthal.de">my homepage</a>.<br/>
  <br/>
</p>

